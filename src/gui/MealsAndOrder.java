    public static void show_meals (){
    
        DefaultTableModel mm =(DefaultTableModel) Table3.getModel();
          Object []row =new Object[3];
          Meal_db m=new Meal_db();
          ArrayList<Meal> list =m.display_mealsss(Restaurant_list.Table2.getModel().getValueAt(Restaurant_list.Table2.getSelectedRow(), 0).toString());
         
           for(int i=0 ;i<list.size();i++)
          {
              row[0]=list.get(i).getName();
              row[1]=list.get(i).getPrice();
              row[2]=list.get(i).getDescription();
              
              mm.addRow(row);
          }
           
    }

