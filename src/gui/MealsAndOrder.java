    private void DoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DoneActionPerformed
        if(evt.getSource()==Done)
        {
            Customer c =new Customer();
            Order order =new Order();
            
            
            c.make_order(notes.getText(), date.getText() ,Integer.parseInt(quantity.getText()), login.username.getText() ,WantMeal.getText());
            order.calc_price();
            
           
            
            Order_db o =new Order_db();
            ArrayList<Order> list =o.display_order();
           
            
            OrderPrice.setText(Integer.toString(o.displayPrice(list.get(list.size()-1).getId())));
        }
    }//GEN-LAST:event_DoneActionPerformed

    
